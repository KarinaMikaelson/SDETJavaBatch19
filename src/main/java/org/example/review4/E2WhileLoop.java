package org.example.review4;

public class E2WhileLoop {
    public static void main(String[] args) {

        char letter = 'A';
        while(letter != 'K'){
            System.out.println(letter);
            letter++;
            //This program will give all letters until 'K'
            // computer uses ASCII chart to know which character or number goes first
        }
    }
}
