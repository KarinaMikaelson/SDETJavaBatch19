package org.example.class3;

public class E1 {
    public static void main(String[] args) {
        float num1=2.3F;
        float num2=5.7F;
        float addNum=num1+num2;
        float subtractNum=num1-num2;
        float multiplyNum=num1*num2;
        float divideNum=num1/num2;

        System.out.print("The subtraction of 2 numbers "+num1+" and "+num2+" is equal to "+subtractNum);
        System.out.print("The addition of 2 numbers "+num1+" and "+num2+" is equal to "+addNum);
        System.out.print("The multiplication of 2 numbers "+num1+" and "+num2+" is equal to "+multiplyNum);
        System.out.print("The division of 2 numbers "+num1+" and "+num2+" is equal to "+divideNum);
    }
}
