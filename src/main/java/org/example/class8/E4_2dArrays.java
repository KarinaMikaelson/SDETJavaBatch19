package org.example.class8;
/*
Loop with Array
 */
public class E4_2dArrays {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++){
            System.out.println("Java is Fun");
            //if we put first loop into the second loop it will repeat itself
        }
        System.out.println(); // to skip the line
        for (int i = 0; i < 5; i++){
            System.out.println("Java is Fun");
        }

        System.out.println();
        for (int i = 0; i < 5; i++) {
            System.out.println("Java is Fun");
        }
    }
}
