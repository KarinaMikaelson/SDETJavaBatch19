public class E5_2dArrays_2 {
    public static void main(String[] args) {
        //7 rows and each row should have 4 *

        for(int j = 0; j < 7; j++){
            for(int i =0; i<=3; i++){
                System.out.print("*");
            }
            System.out.println();

        }
    }
}
