package org.example.class7;

public class E9Arrays {
    public static void main(String[] args) {

        String [] names={"Vinny","Matt","Noodles","James","Maggy"};
        //Enhanced for Loop - NEW LOOP
        //Only works with Arrays
        for(String n:names){
            System.out.println(n);
            // Easiest Loop
            /*
             If i put Method inside the Loop and give it variable (n) and put :names(original variable)
             It will automatically write all values in a right order
             */
        }
    }
}
